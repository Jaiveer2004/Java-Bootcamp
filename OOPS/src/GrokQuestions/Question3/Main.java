package GrokQuestions.Question3;

public class Main {
    public static void main(String[] args) {
        int a = 14856;
        System.out.println(convertToInteger(a));
        System.out.println(convertToString(a));
    }

    // Convertor
    static Integer convertToInteger(int n) {
        Integer a = n;

        return a;
    }

    // Convert to String
    static String convertToString(Integer n) {
        return n.toString();
    }
}
